@pytest.mark.asyncio
async def test_fetch_subscription_nodes_supports_redirects_and_filters(monkeypatch, db_session):
    class MockResponse:
        def __init__(self, text: str):
            self.text = text
            self.is_redirect = False
            self.status_code = 200
            self.headers = {}
            self.url = "https://example.com"
            self.encoding = "utf-8"

        def raise_for_status(self):
            return None

        async def aiter_bytes(self):
            if hasattr(self, 'text'):
                yield self.text.encode()
            else:
                yield b

    class MockClient:
        def __init__(self, *args, **kwargs):
            assert "follow_redirects" not in kwargs
            assert kwargs.get("trust_env") is False

        async def __aenter__(self):
            return self

        async def __aexit__(self, exc_type, exc, tb):
            return False

        def stream(self, method, url, **kwargs):
            import contextlib
            @contextlib.asynccontextmanager
            async def _ctx():
                resp = await self.get(url)
                yield resp
            return _ctx()

        async def get(self, url):
            assert url == "https://example.com/sub"
            return MockResponse(
                "proxies:\n"
                "  - name: HK-1\n"
                "    type: ss\n"
                "    server: 1.1.1.1\n"
                "    port: 443\n"
                "  - name: US-1\n"
                "    type: ss\n"
                "    server: 2.2.2.2\n"
                "    port: 443\n"
            )

    monkeypatch.setattr("app.services.subscription_service.httpx.AsyncClient", MockClient)

    item = Subscription(
        name="sub",
        url="https://example.com/sub",
        is_primary=False,
        node_prefix="TEST",
        filter_regex=["HK"],
    )
    db_session.add(item)
    await db_session.commit()
    await db_session.refresh(item)

    updated = await fetch_subscription_nodes(db_session, item)
    assert [node["name"] for node in updated.raw_nodes] == ["TEST-HK-1"]
    assert [node["name"] for node in updated.source_nodes] == ["HK-1", "US-1"]
    assert updated.fetch_comments == []


