@pytest.mark.asyncio
async def test_generate_yaml_and_script_respect_switches(db_session):
    sub = Subscription(
        name="primary",
        url="https://example.com/sub",
        is_primary=True,
        raw_nodes=[
            {"name": "HK-1", "type": "ss", "server": "1.1.1.1", "port": 443},
            {"name": "US-1", "type": "ss", "server": "2.2.2.2", "port": 443},
        ],
        fetch_comments=["# generated by test"],
    )
    group = NodeGroup(
        name="Proxy",
        group_type="select",
        regex_rules=["HK"],
        include_entries=[{"type": "node", "value": "DIRECT"}],
    )
    db_session.add_all([sub, group])
    await db_session.commit()
    await create_rule(
        db_session,
        RuleCreate(
            name="rules-1",
            category="default",
            type="domain-suffix",
            value="example.com",
            proxy="Proxy",
            options=[],
        ),
    )

    yaml_result = await generate_yaml(db_session, {"enabled": True, "dns": False})
    assert "# generated by test" in yaml_result["yaml"]
    assert "HK-1" in yaml_result["yaml"]
    assert "DOMAIN-SUFFIX,example.com,Proxy" in yaml_result["yaml"]

    script_result = await generate_script(
        db_session,
        {"enabled": True, "node_groups": True, "rules": True, "dns": False, "exclude_node_proxies": True},
    )
    assert "const modules =" in script_result["script"]
    assert '"name": "Proxy"' in script_result["script"]
    assert '"proxies": [' in script_result["script"]
    assert '"HK-1"' not in script_result["script"]


